#include "E01.h"

void E01()
{
	// C의 문자 출력 방식
	printf("Hello World!\n");
	// \는 키보드 기준 원표시 W에 -그어져 있는 그것
	/*
	printf는 C에서 제공하는 콘솔창에 문자를 출력하는
	기능이다.

	\n은 개행문자라고도 부르며, 엔터키(줄바꿈)을
	담당한다.
	*/

	// C++의 문자 출력 방식
	cout << "Hello World!" << endl;
	/*
	cout은 C++에서 제공하는 콘솔창 출력 기능이며,
	endl은 \n처럼 엔터키(줄바꿈)을 담당하는 기능이다.
	*/
}
